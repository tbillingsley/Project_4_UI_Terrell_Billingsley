    {
        // declare a double variable to hold the radius
        double radius = Double.parseDouble(jTextField1.getText());
        //create a Circle object
        Circle c = new Circle(radius);
        //display results
        jTextField2.setText(c.getFormattedCircumference());
        jTextField3.setText(c.getFormattedArea());
    }
